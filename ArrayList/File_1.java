package ArrayList;

import java.util.ArrayList;

//ArrayList is a resizeable array found in java.util
public class File_1 {
    public static void main(String[] args) {
        ArrayList<String> Cars = new ArrayList<String>();
        Cars.add("Toyota");
        Cars.add("Honda");
        Cars.add("KIA");
        for (String i : Cars) {
            System.out.println(i);
        }
    }
}

